package fundamentos;

public class ConversaoPrimitivos {

	public static void main(String[] args) {
		
		double a = 1;
		System.out.println(a);
		
		//Casting
		float b = (float) 1.1234568;
		System.out.println(b);
		
		int c = 4;
		byte d = (byte) c;
		System.out.println(d);
		
		double e = 1.0;
		int f = (int) e;
		System.out.println(f);
	}
}
